"""
File Handling
Week 2 Examples
"""

# =======================================================
# Example 1: Write
# =======================================================
with open('notes.txt','w') as f:f.write('Experiment')

# =======================================================
# Example 2: Read
# =======================================================
with open('notes.txt') as f:print(f.read())

# =======================================================
# Example 3: Append
# =======================================================
with open('notes.txt','a') as f:f.write('\nVoltage=230V')

# =======================================================
# Example 4: Read Lines
# =======================================================
with open('notes.txt') as f:print(f.readlines())

# =======================================================
# Example 5: Count
# =======================================================
with open('notes.txt') as f:print(len(f.readlines()))

